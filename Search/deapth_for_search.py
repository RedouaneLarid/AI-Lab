class Checkpoint:
    def __init__(self , maze , parent , state , action):
        self.maze = maze
        self.parent = parent 
        self.state = state 
        self.action = action
    
    @staticmethod
    def findStart(self):
        for i in range(len(self.maze)):
            for j in range(len(self.maze[0])):
                if self.maze[i][j] == 3:
                    return [i , j]
                
    @staticmethod
    def moveDown(self):
        return [self.state[0] + 1 , self.state[1]] if self.maze[self.state[0] + 1][self.state[1]] == 1 or self.maze[self.state[0] + 1][self.state[1]] == 4 else self.state

    @staticmethod
    def moveUp(self):
        return [self.state[0] - 1 , self.state[1]] if self.maze[self.state[0] - 1][self.state[1]] == 1 or self.maze[self.state[0] - 1][self.state[1]] == 4 else self.state
    
    @staticmethod
    def moveRight(self):
        return [self.state[0] , self.state[1] + 1] if self.maze[self.state[0]][self.state[1] + 1] == 1 or self.maze[self.state[0]][self.state[1] + 1] == 4 else self.state
    
    @staticmethod
    def moveLeft(self):
        return [self.state[0] , self.state[1] - 1] if self.maze[self.state[0]][self.state[1] - 1] == 1 or self.maze[self.state[0]][self.state[1] - 1] == 4 else self.state
    
    def move(self , action):
        self.state = action(self)
        self.maze[self.state[0]][self.state[1]] = 9 if self.maze[self.state[0]][self.state[1]] != 4 else 4 
        os.system("cls")
        self.draw()

    def terminal(self):
        return self.maze[self.state[0]][self.state[1]] == 4
        

    def possible_directions(self):
        possible_directions = []

        if self.state != self.moveDown(self):
            possible_directions.append(self.moveDown)
        
        if self.state != self.moveUp(self):
            possible_directions.append(self.moveUp)

        if self.state != self.moveRight(self):
            possible_directions.append(self.moveRight)

        if self.state != self.moveLeft(self):
            possible_directions.append(self.moveLeft)

        return possible_directions
    
    def draw(self):
        for row in self.maze:
            for col in row:
                if col == 0:
                    print("#" , end=" ")
                if col == 1:
                    print(" " , end=" ")
                if col == 3:
                    print("S" , end=" ")
                if col == 4:
                    print("G" , end=" ")
                if col == 9:
                    print("\033[0;31m*\033[1;37m" , end=" ")

            print("")

class StackFrontier:
    def __init__(self):
        self.memory = []

    def add(self , item):
        self.memory.append(item)

    def take(self):
        if self.is_empty():
            raise Exception("The frontier is empty")
        else:
            return self.memory.pop()

    def is_empty(self):
        return len(self.memory) == 0
    
class QueueFrontier:
    def __init__(self):
        self.memory = []

    def add(self , item):
        self.memory.append(item)

    def take(self):
        if self.is_empty():
            raise Exception("The frontier is empty")
        else:
            return self.memory.pop(0)

    def is_empty(self):
        return len(self.memory) == 0

